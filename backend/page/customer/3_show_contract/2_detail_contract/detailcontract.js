  return new Date(d.toISOString().split("T")[0]); // ‡∏ï‡∏±‡∏î‡πÄ‡∏ß‡∏•‡∏≤ ‡πÄ‡∏´‡∏•‡∏∑‡∏≠ YYYY‚ÄëMM‚ÄëDD
